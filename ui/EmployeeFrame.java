    private void numberInputOnly(KeyEvent evt){
            char ch = evt.getKeyChar() ;

            if ( !((ch == KeyEvent.VK_BACK_SPACE) || (ch == KeyEvent.VK_DELETE)
                ||  (ch == KeyEvent.VK_ENTER)     || (ch == KeyEvent.VK_TAB)
                ||  (Character.isDigit(ch))))
            {
               evt.consume() ;
            }
    }
