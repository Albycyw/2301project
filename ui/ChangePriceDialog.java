    private void changePrice(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePrice
        try
        {
            String cat = jTextField1.getText();
            String form = jTextField2.getText();
            int newPrice = Integer.parseInt(jTextField3.getText());
            PriceSchemeManagement priceMan = new PriceSchemeManagement();
            priceMan.setPrice(cat, form, newPrice);
            this.hide();
        }
        catch(SQLException e)
        {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(),
                    "Database Error.", 0);
        }
        catch(IOException e)
        {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(),
                    "Bad input", 0);
        }
        catch(ClassNotFoundException e)
        {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(),
                    "Database Driver Problem", 0);
        }
    }//GEN-LAST:event_changePrice

